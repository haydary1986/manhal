// promotionTable2Screen renders جدول رقم (٢): activities & community service.
func promotionTable2Screen() Screen {
	var b strings.Builder
	b.WriteString("📋 الجدول الثاني — النشاطات وخدمة المجتمع\n")
	b.WriteString("تعليمات الترقيات العلمية 2025-2026\n\n")
	for _, it := range promotionTable2Items {
		b.WriteString(intToAr(it.No) + ". " + it.Title + " — " + it.Points + "\n")
	}
	b.WriteString("\nℹ️ النقاط استرشادية وفق التعليمات الرسمية، والقرار النهائي للجنة الترقيات.")
	return Screen{Text: b.String(), Keyboard: promoTablesNav()}
}

// intToAr renders a small integer using Eastern-Arabic digits for the list.
func intToAr(n int) string {
	digits := []rune("٠١٢٣٤٥٦٧٨٩")
	if n == 0 {
		return string(digits[0])
	}
	var out []rune
	for n > 0 {
		out = append([]rune{digits[n%10]}, out...)
		n /= 10
	}
	return string(out)
}
