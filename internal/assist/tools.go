// Package assist defines the AI assistant tools (Phase 3). Each tool is a named
// prompt template: a system prompt that turns the shared LLM provider into a
// specific academic helper. The bot pairs a tool's System prompt with the
// user's text and sends it to the provider.
package assist

// Tool is one AI assistant capability.
